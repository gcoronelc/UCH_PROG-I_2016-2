USE UCH;

CREATE TABLE NOTAS(
   IDALU INT NOT NULL auto_increment,
   NOMBRE VARCHAR(80),
   NOTA1  INT,
   NOTA2  INT,
   PROM   INT,
   PRIMARY KEY (IDALU)
);


