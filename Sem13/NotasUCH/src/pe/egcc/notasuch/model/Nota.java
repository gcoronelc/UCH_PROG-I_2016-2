package pe.egcc.notasuch.model;

public class Nota {

  private int idalu;
  private String nombre;
  private int nota1;
  private int nota2;
  private int prom;

  public Nota() {
  }

  public int getIdalu() {
    return idalu;
  }

  public void setIdalu(int idalu) {
    this.idalu = idalu;
  }

  public String getNombre() {
    return nombre;
  }

  public void setNombre(String nombre) {
    this.nombre = nombre;
  }

  public int getNota1() {
    return nota1;
  }

  public void setNota1(int nota1) {
    this.nota1 = nota1;
  }

  public int getNota2() {
    return nota2;
  }

  public void setNota2(int nota2) {
    this.nota2 = nota2;
  }

  public int getProm() {
    return prom;
  }

  public void setProm(int prom) {
    this.prom = prom;
  }

}
