  private void cboTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboTipoActionPerformed
    if(estoyOcupado){
      return;
    }
    estoyOcupado = true;
    String tipo = cboTipo.getSelectedItem().toString();
    cboCargo.setEnabled(tipo.equals("EMPLEADO"));
    txtHoras.setEnabled(tipo.equals("DOCENTE"));
    estoyOcupado = false;
  }//GEN-LAST:event_cboTipoActionPerformed

  private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
    // Datos
    Model model = new Model();
    model.setTipo(cboTipo.getSelectedItem().toString());
    switch(model.getTipo()){
      case "DOCENTE":
        model.setHoras(Integer.parseInt(txtHoras.getText()));
        break;
      case "EMPLEADO":
        model.setCargo(cboCargo.getSelectedItem().toString());
        break;
    }
    // Proceso
    control.procesar(model);
    // Reporte
    String repo = "";
    repo += "Sueldo: " + model.getSueldo() + "\n";
    repo += "Bono: " + model.getBono()+ "\n";
    JOptionPane.showMessageDialog(rootPane, repo);
  }//GEN-LAST:event_btnProcesarActionPerformed

