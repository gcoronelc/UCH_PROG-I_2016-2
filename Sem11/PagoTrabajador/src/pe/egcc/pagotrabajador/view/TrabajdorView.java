  private void llenarComboTipo(){
    estoyOcupado = true;
    cboTipo.removeAllItems();
    for(String tipo: control.getTipos()){
      cboTipo.addItem(tipo);
    }
    estoyOcupado = false;
  }
  
  private void llenarComboCargo(){
    cboCargo.removeAllItems();
    for(String tipo: control.getCargos()){
      cboCargo.addItem(tipo);
    }
    
  }
  
  private void initForm(){
    estoyOcupado = true;
    cboTipo.setSelectedIndex(-1);
    cboCargo.setSelectedIndex(-1);
    cboCargo.setEnabled(false);
    txtHoras.setText("");
    txtHoras.setEnabled(false);
    estoyOcupado = false;
  }

