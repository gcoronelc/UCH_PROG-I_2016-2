  private void cboCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboCategoriaActionPerformed
    if( estoyOcupado ){
      return;
    }
    CategoriaModel model = (CategoriaModel) cboCategoria.getSelectedItem();
    lblCosto.setText(String.valueOf(model.getCosto()));
    lblPagoHora.setText(String.valueOf(model.getPagoHora()));

  }//GEN-LAST:event_cboCategoriaActionPerformed

  private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
    // Datos
    CursoModel model = new CursoModel();
    model.setNombre(txtCurso.getText());
    model.setVacantes(Integer.parseInt(txtVacantes.getText()));
    CategoriaModel cat = (CategoriaModel) cboCategoria.getSelectedItem();
    model.setCategoria(cat.getCategoria());
    // Proceso
    control.procesar(model);
    // Reporte
    String repo = "";
    repo += "Ingresos: " + model.getIngresos() + "\n";
    repo += "Gastos: " + model.getGastos() + "\n";
    repo += "Rentabilidad: " + model.getRentabilidad() + "\n";
    JOptionPane.showMessageDialog(rootPane, repo);
  }//GEN-LAST:event_btnProcesarActionPerformed

